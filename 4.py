n1, n2 = map(int, input().split())  # Читаем размеры массивов
arr1 = [int(input()) for _ in range(n1)]  # Читаем первый массив
arr2 = [int(input()) for _ in range(n2)]  # Читаем второй массив

i = j = count = 0  # Инициализируем указатели и счетчик
while i < n1 and j < n2:  # Пока не дошли до конца одного из массивов
    if arr1[i] == arr2[j]:  # Если элементы равны
        count += 1  # Увеличиваем счетчик
        i += 1  # Двигаем оба указателя
        j += 1
    elif arr1[i] < arr2[j]:  # Если элемент первого массива меньше
        i += 1  # Двигаем только первый указатель
    else:  # Если элемент второго массива меньше
        j += 1  # Двигаем только второй указатель

print(count)  # Выводим количество общих элементов