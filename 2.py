k = int(input())  # Читаем количество клавиш, которые может нажать каждый игрок
field = [input().strip() for _ in range(4)]  # Читаем игровое поле 4x4
count = [0] * 10  # Массив для подсчета количества каждой цифры (0-9)

# Подсчитываем количество каждой цифры на поле
for row in field:
    for char in row:
        if char != '.':  # Если это цифра, а не точка
            count[int(char)] += 1  # Увеличиваем счетчик для этой цифры

total = 0  # Счетчик баллов
for t in range(1, 10):  # Проверяем цифры от 1 до 9
    # Если цифра t встречается и ее можно нажать вдвоем (2*k)
    if 0 < count[t] <= 2 * k:
        total += 1  # Добавляем балл

print(total)  # Выводим общее количество баллов