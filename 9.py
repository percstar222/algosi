n, m = map(int, input().split())  # Читаем исходное и конечное количество деревьев

if m == 0:  # Если нужно вырубить все деревья
    print(1)  # Только 1 способ
elif m == 1:  # Если нужно оставить 1 дерево
    print(n)  # n способов (по количеству деревьев)
else:
    count = 0  # Счетчик способов
    # Перебираем возможные расстояния между оставшимися деревьями
    for d in range(1, n):
        # Проверяем, можно ли разместить m деревьев с шагом d
        if (m - 1) * d < n:
            # Добавляем количество вариантов для этого d
            count += n - (m - 1) * d
    print(count)  # Выводим общее количество способов