a = input().strip()  # Читаем первое число
op = input().strip()  # Читаем операцию
b = input().strip()  # Читаем второе число

num_a = int(a)  # Преобразуем в число
num_b = int(b)  # Преобразуем в число

if op == '+':  # Если операция сложения
    result = num_a + num_b  # Выполняем сложение
else:  # Если операция вычитания
    result = num_a - num_b  # Выполняем вычитание

print(result)  # Выводим результат